#include<iostream>

using namespace std;

#define N 8 
#define B 4 

void mulMat(int mat1[][N], int mat2[][N])
{
    int result[N][N];

    cout << "Multiplication of given two matrices is:\n";

    for (int i = 0; i < N; i++) {
        for (int j = 0; j < N; j++) {
            result[i][j] = 0;
            for (int k = 0; k < N; k++) {
                result[i][j] += mat1[i][k] * mat2[k][j];
                cout << "mat1["<<i<<"]["<<k<<"] && mat2["<<k<<"]["<<j<<"] "<< endl;
            }
            cout << "====" << endl;
        }
            cout << "-------------------" << endl;
        
    }
}  

void matmulBlocked(int mat1[][N], int mat2[][N])
{
    int result[N][N];

    for (int jj = 0; jj < N; jj += B) {
        for (int kk = 0; kk < N; kk +=B) {
            for (int i=0; i < N; i++) {
                for (int j=jj; j<min(jj+B, N); j++) {
                    int r = 0;
                    for (int k=kk; k<min(kk+B, N); k++) {
                        r += mat1[i][k] * mat2[k][j];
                        cout <<"mat1["<<i<<"]["<<k<<"] && mat2["<<k<<"]["<<j<<"] "<<endl;
                        // cout << "mat1[" << i << "]" << "  " << "mat2[" << k << "]" << endl; 
                    }
                    result[i][j] += r;
                    cout << "result[" << i << "]" << endl; 
                    cout << "====" << endl;
                }
            }
        }
    }
}

